package string;
/**
 * int length()
 * 获取当前字符串的长度（字符个数）
 *
 *
 */
public class LengthDemo {
    public static void main(String[] args) {
        String str="我爱我家";
        int len=str.length();
        System.out.println("我爱我家的长度为："+len);
        String str1="同一个世界，同一个梦想";
        int length=str1.length();//int length 获取字符串的长度也就是字符串的个数
        System.out.println(length);
    }
}
